# Addition Assignment

x = 10
x += 5
print(x)  # Output: 15


# Subtraction Assignment

x = 15
x -= 5
print(x)  # Output: 10

# Multiplication Assignment

x = 20
x *= 3
print(x)  # Output: 60

# Division Assignment 

x = 25
x /= 5
print(x)  # Output: 5.0

# Modulus Assignment

x = 34
x %= 7
print(x)  # Output: 6

